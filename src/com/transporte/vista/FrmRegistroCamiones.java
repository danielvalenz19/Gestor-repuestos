    void agregar() {
        String matricula = txtMatricula.getText();
        String color = txtColor.getText();
        Object[] ob = new Object[2];
        ob[0] = matricula;
        ob[1] = color;
        CamionDAO.getCamionDAO().agregar(ob);
    }

    public void limpiar() {
        txtId.setText("");
        txtMatricula.setText("");
        txtColor.setText("");
    }

    void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

