    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            PreparedStatement ps = cn.prepareStatement("INSERT INTO repuestos (nombre,cantidad,descripcion) VALUES(?,?,?)");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtCantidad.getText());
            ps.setString(3, txtDescripcion.getText());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "DATOS GUARDADOS!...");
            mostrarDatos();
            limpiar();

        } catch (SQLException e) {
            System.out.println("ERROR AL GUARDAR LOS DATOS " + e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

