    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            PreparedStatement ps = cn.prepareStatement("INSERT INTO camiones (matricula,color) VALUES(?,?)");
            ps.setString(1, txtMatricula.getText());
            ps.setString(2, txtColor.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "DATOS GUARDADOS!...");
            mostrarDatos();
            limpiar();

        } catch (SQLException e) {
            System.out.println("ERROR AL GUARDAR LOS DATOS " + e);
        }    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked

        btnEditar.setEnabled(true);
        btnRegistrar.setEnabled(false);
        int fila = this.TablaDatos.getSelectedRow();
        this.txtId.setText(this.TablaDatos.getValueAt(fila, 0).toString());
        this.txtMatricula.setText(this.TablaDatos.getValueAt(fila, 1).toString());
        this.txtColor.setText(this.TablaDatos.getValueAt(fila, 2).toString());

    }//GEN-LAST:event_TablaDatosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            PreparedStatement ps = cn.prepareStatement("Update camiones set matricula='" + txtMatricula.getText() + "', color='" + txtColor.getText() + "' where id='" + txtId.getText() + "'");
            int indice = ps.executeUpdate();

            if (indice > 0) {
                JOptionPane.showMessageDialog(rootPane, "DATOS ACTUALIZADOS CORRECTAMENTE");
                mostrarDatos();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "NO SE SELECCIONO FILA");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR DATOS " + e);
        }
     }//GEN-LAST:event_btnEditarActionPerformed

